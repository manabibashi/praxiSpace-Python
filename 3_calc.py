"""
3. 計算する。

+ - * / で計算できます。
"""

# 計算結果を画面に出します。
print(1 + 1)

# 変数を使って計算します。
x = 3
y = 2
print(f"足し算：{x + y}")
print(f"引き算：{x - y}")
print(f"かけ算：{x * y}")
print(f"割り算：{x / y}")

# 変数の値を計算で変えます。
age = 10
print(f"年は{age}さいです。")
age = age + 1
print(f"来年は{age}さいになります。")
age += 1  # age = age + 1 と同じです。
print(f"再来年は{age}さいになります。")
